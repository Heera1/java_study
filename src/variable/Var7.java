package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; //정수 //마이너스도 가능
        double b = 10.5; //실수 //마이너스도 가능
        boolean c = true; //불리언
        char d = 'D'; //문자 하나 //실무에서 잘 사용하지 않음
        String e = "Hello World"; //문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
